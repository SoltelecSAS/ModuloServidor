    private void cargarTabla() {
        DefectosJpaController mprop = new DefectosJpaController();
        modelo = new DefaultTableModel();

        List<Object[]> results1 = mprop.findTDefectos1();

        // Ahora sólo hay que rellenar el DefaultTableModel con los datos del ResultSet. La forma "manual" de hacer esto es la siguiente
        // Creamos las columnas.
        modelo.addColumn("Serial");
        modelo.addColumn("Nombre");
        modelo.addColumn("Tipo");
        modelo.addColumn("Tipo Prueba");
        modelo.addColumn("Grupo");

        // Bucle para cada resultado en la consulta

        for (int i = 0; i < results1.size(); i++) {

            // Se crea un array que será una de las filas de la tabla.
            Object[] fila; // Hay tres columnas en la tabla
            fila = results1.get(i);

            modelo.addRow(fila);
            tblDefectos.setModel(modelo);
        }
    }    

