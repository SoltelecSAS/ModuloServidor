    private void cargarTabla() {
        cJpaPropietario = new PropietariosJpaController();
        results = cJpaPropietario.findPropietarios1();
        cargarTabla(results);
    }

    private void cargarTabla(List<Propietarios> results) {
        modelo = new DefaultTableModel();

        // Ahora s칩lo hay que rellenar el DefaultTableModel con los datos del ResultSet. La forma "manual" de hacer esto es la siguiente
        // Creamos las columnas.
        modelo.addColumn("Tipo de Documento");
        modelo.addColumn("Nro de Documento");
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido");
        modelo.addColumn("Direccion");
        modelo.addColumn("Telefono");
        modelo.addColumn("Tipo de Licencia");
        modelo.addColumn("Nro Licencia");

        for (Propietarios result : results) {
            // Se crea un array que ser치 una de las filas de la tabla.
            Object[] fila = new Object[8]; // Hay tres columnas en la tabla
            // Se rellena cada posici칩n del array con una de las columnas de la tabla en base de datos.
            fila[0] = result.getTipoIdentificacion();
            fila[1] = result.getCarowner();
            fila[2] = result.getNombres();
            fila[3] = result.getApellidos();
            fila[4] = result.getDireccion();
            fila[5] = result.getNumerotelefono();
            fila[6] = result.getTipolicencia();
            fila[7] = result.getNumerolicencia();
            // Se a침ade al modelo la fila completa.
            modelo.addRow(fila);
        }
        tblPropietarios.setModel(modelo);
    }

    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed

        String dato = txtValor.getText();
        String elemento = String.valueOf(cmbCampo.getSelectedItem());
        String campo = "";
        String condicion = "=";

        cJpaPropietario = new PropietariosJpaController();

        switch (elemento) {
            case "Buscar Todos":
                cargarTabla();
                return;
            case "Nro de Documento":
                if (!Validaciones.isNumeric(dato)) {
                    JOptionPane.showMessageDialog(rootPane, "Para el Nro de Documento solo se aceptan numeros", "Advertencia", JOptionPane.WARNING_MESSAGE);
                    return;
                }
                campo = "carowner";
                condicion = "=";
                break;
            case "Nro de Licencia":
                campo = "numerolicencia";
                dato += "%";
                condicion = "like";
                break;
            case "Nombres":
                campo = "nombres";
                dato += "%";
                condicion = "like";
                break;
            case "Apellidos":
                campo = "apellidos";
                dato += "%";
                condicion = "like";
                break;
        }

        if (!CValidaciones.validarCampos(pnlConsulta)) {
            return;
        }

        results = cJpaPropietario.findVehiculos_porParametros(dato, campo, condicion);
        cargarTabla(results);
    }//GEN-LAST:event_btnAceptarActionPerformed

