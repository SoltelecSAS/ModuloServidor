    private void cargarTabla() {
        TiposMedidaJpaController mprop = new TiposMedidaJpaController();
        modelo = new DefaultTableModel();

        List<TiposMedida> results1 = mprop.findTMedida1();

        // Ahora s칩lo hay que rellenar el DefaultTableModel con los datos del ResultSet. La forma "manual" de hacer esto es la siguiente
        // Creamos las columnas.
        modelo.addColumn("Tipo Medida");
        modelo.addColumn("Tipo Prueba");
        modelo.addColumn("Nombre");
        modelo.addColumn("Descripcion");
        modelo.addColumn("Unidad");

        for (TiposMedida results11 : results1) {
            // Se crea un array que ser치 una de las filas de la tabla.
            Object[] fila = new Object[5]; // Hay tres columnas en la tabla
            // Se rellena cada posici칩n del array con una de las columnas de la tabla en base de datos.
            fila[0] = results11.getMeasuretype();
            fila[1] = results11.getTesttype();
            fila[2] = results11.getNombremedida();
            fila[3] = results11.getDescripcionmedida();
            fila[4] = results11.getUnidad();
            // Se a침ade al modelo la fila completa.
            modelo.addRow(fila);
        }

        tblMedida.setModel(modelo);
    }


