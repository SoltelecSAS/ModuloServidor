    private void tblVehiculosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblVehiculosMouseClicked
        int fila = tblVehiculos.rowAtPoint(evt.getPoint());
        int columna = 0;
        int resultado = 0;
        if ((fila > -1) && (columna > -1)) {
            try {
                resultado = Integer.parseInt(String.valueOf(modelo.getValueAt(fila, columna)));
            } catch (NumberFormatException ex) {
                LOG.severe(ex.getMessage());
            }
        }

        try {
            FrmPruebasDatos eda = new FrmPruebasDatos(resultado, 1, this);
            System.out.println("resu  " + resultado);
            Principal.desktop.add(eda);

            eda.toFront();
            eda.setMaximum(true);
            eda.setVisible(true);
            this.dispose();
            this.setVisible(false);
        } catch (PropertyVetoException ex) {
            LOG.severe(ex.getMessage());
        }

    }//GEN-LAST:event_tblVehiculosMouseClicked


