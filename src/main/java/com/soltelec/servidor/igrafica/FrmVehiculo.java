    private void cargarTabla() {
        results = vehiculosController.findVehiculos();
        cargarTabla(results);
    }

    private void cargarTabla(List<Vehiculos> results) {
        modelo = new DefaultTableModel();

        // Ahora sólo hay que rellenar el DefaultTableModel con los datos del ResultSet. La forma "manual" de hacer esto es la siguiente
        // Creamos las columnas.
        modelo.addColumn("Serial");
        modelo.addColumn("Placa");
        modelo.addColumn("Marca");
        modelo.addColumn("Modelo");
        modelo.addColumn("Cilindraje");
        modelo.addColumn("Nro Licencia");
        modelo.addColumn("Nro Soat");

        for (Vehiculos result : results) {
            // Se crea un array que será una de las filas de la tabla.
            Object[] fila = new Object[7]; // Hay tres columnas en la tabla
            // Se rellena cada posición del array con una de las columnas de la tabla en base de datos.
            fila[0] = result.getCar();
            fila[1] = result.getCarplate();
            fila[2] = result.getMarcas().getNombremarca();
            fila[3] = result.getModelo();
            fila[4] = result.getCilindraje();
            fila[5] = result.getNumerolicencia();
            fila[6] = result.getNumeroSOAT();
            // Se añade al modelo la fila completa.
            modelo.addRow(fila);
        }
        tblVehiculos.setModel(modelo);
    }

    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed

        String valor = txtValor.getText();
        String elemento = String.valueOf(cmbCampo.getSelectedItem());
        String campo = "";
        String condicion = "like";

        //cJpaPropietario = new PropietariosJpaController();
        switch (elemento) {
            case "Buscar Todos":
                System.out.println("entro a Buscar Todos");
                cargarTabla();
                return;
            case "Placa":
                System.out.println("entro a placa");
                campo = "v.carplate";
                condicion = "=";
                break;
            case "Nro de Licencia":
                System.out.println("entro a Nro de Licencia");
                campo = "v.numerolicencia";
                valor += "%";
                break;
            case "Nro Soat":
                System.out.println(" entro a Nro Soat");
                campo = "v.numeroSOAT";
                valor += "%";
                break;
        }

        if (!CValidaciones.validarCampos(pnlConsulta)) {
            return;
        }
                System.out.println(campo+ ", "+ valor + "," + condicion);
        results = vehiculosController.findVehiculosByParameter(campo, valor, condicion);
        cargarTabla(results);
    }//GEN-LAST:event_btnAceptarActionPerformed

