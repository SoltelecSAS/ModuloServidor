    private void btn_procesoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_procesoActionPerformed
        if (txt_nombreProceso.getText().length() > 20 || txt_nombreProceso.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Solo 20 caracteres y no puede estar vacio");
            return;
        }
        if (esMarca) {
            if (editando) {
                try {
                    MarcasJpaController marcasJpaController = new MarcasJpaController();
                    getMarcaGlobal().setNombremarca(txt_nombreProceso.getText());
                    marcasJpaController.edit(getMarcaGlobal());
                    labl_marca.setText(getMarcaGlobal().getNombremarca());
                    JOptionPane.showMessageDialog(this, "Se ha editado el registro");
                } catch (Exception ex) {
                    LOG.severe(ex.getMessage());
                    JOptionPane.showMessageDialog(this, "No se ha editado el registro " + ex);
                }
            } else {
                try {
                    MarcasJpaController marcasJpaController = new MarcasJpaController();
                    setMarcaGlobal(new Marcas());
                    getMarcaGlobal().setNombremarca(txt_nombreProceso.getText());
                    int idMarquita = marcasJpaController.ultimoIdMarca();
                    getMarcaGlobal().setCarmark(idMarquita + 1);
                    idMarcaPrincipal = getMarcaGlobal().getCarmark();
                    labl_marca.setText(getMarcaGlobal().getNombremarca());
                    cargarLineas();
                    marcasJpaController.create(getMarcaGlobal());
                    JOptionPane.showMessageDialog(this, "Se ha creado el registro");
                    btn_nuevaLinea.setEnabled(true);
                    btn_eliminarMarca.setEnabled(true);
                    btn_editarLinea.setEnabled(false);
                    btn_eliminarLinea.setEnabled(false);
                } catch (Exception ex) {
                    LOG.severe(ex.getMessage());
                    JOptionPane.showMessageDialog(this, "No se ha creado el registro " + ex);
                }
            }
        }
        dialogo_marca.setVisible(false);
    }//GEN-LAST:event_btn_procesoActionPerformed
    private void cargarLineas() {
        Object[] filaLinea;
        tableModelLinea = new DefaultTableModel();
        tableModelLinea.addColumn("Clave");
        tableModelLinea.addColumn("Linea");
        tableModelLinea.addColumn("Run");
        List<LineasVehiculos> listLineas = lineasJpa.buscarLineasPorMarca(idMarcaPrincipal);
        if (!listLineas.isEmpty()) {
            for (LineasVehiculos lineasVehiculos : listLineas) {
                filaLinea = new Object[3];
                filaLinea[0] = lineasVehiculos.getCarline();
                filaLinea[1] = lineasVehiculos.getCrlname();
                filaLinea[2] = lineasVehiculos.getCrlcod();
                tableModelLinea.addRow(filaLinea);
            }

        }
        table_lineas.setModel(tableModelLinea);
    }

