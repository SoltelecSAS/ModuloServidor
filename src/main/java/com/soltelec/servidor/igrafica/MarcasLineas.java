    private void btn_guardarLineaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarLineaActionPerformed
        if (editandoLinea) {
            try {
                if (!CValidaciones.validarCampos(pnlLinea)) {
                    return;
                }
                LineasVehiculosJpaController lineasController = new LineasVehiculosJpaController();
                getLineasVehiculosGlobal().setCrlname(txt_nombreLinea.getText());
                getLineasVehiculosGlobal().setCrlcod(txt_run.getText());
                lineasController.edit(getLineasVehiculosGlobal());
                JOptionPane.showMessageDialog(this, "Se ha editado el registro");
            } catch (Exception ex) {
                LOG.severe(ex.getMessage());
                JOptionPane.showMessageDialog(this, "No se ha editado el registro " + ex);
            }

        } else {
            LineasVehiculosJpaController lineasController = new LineasVehiculosJpaController();
            setLineasVehiculosGlobal(new LineasVehiculos());
            try {
                if (!CValidaciones.validarCampos(pnlLinea)) {
                    return;
                }
                getLineasVehiculosGlobal().setMarcas(new Marcas(idMarcaPrincipal));
                getLineasVehiculosGlobal().setCarline(lineasController.ultimoIdLinea() + 1);
                getLineasVehiculosGlobal().setCrlname(txt_nombreLinea.getText());
                getLineasVehiculosGlobal().setCrlcod(txt_run.getText());

                lineasController.create(getLineasVehiculosGlobal());
                CMensajes.mensajeCorrecto("Se ha creado el registro");
                CValidaciones.limpiarCampos(pnlLinea);
            } catch (Exception ex) {
                LOG.severe(ex.getMessage());
                CMensajes.mensajeError("No se ha creado el registro");
                CMensajes.mostrarExcepcion(ex);
            }
        }
        cargarLineas();
        btn_editarLinea.setEnabled(false);
        btn_eliminarLinea.setEnabled(false);
        dialogo_linea.setVisible(false);
    }//GEN-LAST:event_btn_guardarLineaActionPerformed

