    private void btn_eliminarMarcaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarMarcaActionPerformed
        int test = JOptionPane.showConfirmDialog(this, "Se va a eliminar el registro.");
        if (test != JOptionPane.OK_OPTION) {
            return;
        }

        try {
            MarcasJpaController marcasJpaController = new MarcasJpaController();
            marcasJpaController.destroy(idMarcaPrincipal);
            JOptionPane.showMessageDialog(this, "Se ha eliminado el registro ");
            labl_marca.setText(null);
            idMarcaPrincipal = -1;
            btn_editarMarca.setEnabled(false);
            btn_eliminarMarca.setEnabled(false);
            btn_editarLinea.setEnabled(false);
            btn_eliminarLinea.setEnabled(false);
            btn_nuevaLinea.setEnabled(false);
            cargarLineas();
        } catch (IllegalOrphanException | NonexistentEntityException | HeadlessException e) {
            LOG.severe(e.getMessage());
            JOptionPane.showMessageDialog(this, "No se ha eliminado el registro ya que esta referenciado en otro registro");
        }
    }//GEN-LAST:event_btn_eliminarMarcaActionPerformed

